#include <oop.h>

#define TYPENAME Object

////////////////////////////////////////////////////////////////////////////////
Object *_(Construct)(void *object)
{
  if (this) {
    this->base = object;
  }
  
  return this;
}

////////////////////////////////////////////////////////////////////////////////
void _(Destruct)()
{
  if (this) {
    DELETE (this->base);
  }
}

////////////////////////////////////////////////////////////////////////////////
void *CONST (Get)()
{
  return this->base;
}